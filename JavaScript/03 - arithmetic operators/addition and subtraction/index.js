// Adição e concatenação

adicao = 1 + 2;
console.log(adicao);

concatenacao = "Giulia" + "Nogueira";
console.log(concatenacao);

adicaoEConcatenacao = "Giuia" + " tem " + (20 + 1) + " anos";
console.log(adicaoEConcatenacao);



// Subtração

