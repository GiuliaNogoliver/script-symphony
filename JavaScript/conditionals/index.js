// Condicional If Else

let variavel = 4;

if (variavel <= 0){
    console.log("A variavél é nula ou negativa");
} else {
    console.log("A variavél nao é nula ou negativa");
}

let variavelDois = 1;

if (variavelDois == variavel){
    console.log("As variaveis sao iguais");
} else if (variavelDois > variavel){
    console.log("A variavel dois é mais que a primeira");
} else {
    console.log("As variaveis sao diferentes");
}