// Atribuição

//  =, +=, -=, *=, /=, %=
//  let e var -> trabalha por excopo, pode ser alterado
//  const -> é constante, nao pode ser alterado

let a = 6;
console.log(a);

let b = 2;
console.log(b);

a += b;
console.log(a);

a -= b;
console.log(a);

a *= b;
console.log(a);

a /= b;
console.log(a);

a %= b;
console.log(a);

//  o valor esta sendo alterado e manipulado linha por linha, naoi reseta o valor da variavel antes de cada operacao