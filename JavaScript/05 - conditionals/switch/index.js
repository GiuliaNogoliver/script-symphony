// Condicional Switch

const mes = 5;

switch (mes) {
    case 1: console.log("Janiero");
        break;
    case 2: console.log("Fevereiro");
        break;
    case 3: console.log("Março");
        break;
    case 4: console.log("Abril");
        break;
    case 5: console.log("Maio");
        break;                          // break é encontrei fiz fechei
}
